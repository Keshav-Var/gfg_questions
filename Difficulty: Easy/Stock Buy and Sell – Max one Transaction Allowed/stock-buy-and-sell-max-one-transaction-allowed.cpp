class Solution {
  public:
    int maximumProfit(vector<int> &arr) {
        int res = 0;
        int minimum = arr[0];
        for(int i = 1; i < arr.size(); i++){
            if(arr[i] < minimum)
            minimum = arr[i];
            res = max(res, (arr[i] - minimum));
        }
        return res;
    }
};


