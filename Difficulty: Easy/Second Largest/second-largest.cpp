class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        int largest = -1;
        int secLargest = -1;
        for(auto it:arr){
            if(it > largest){
                secLargest = largest;
                largest = it;
            }
            else if(it > secLargest && it != largest){
                secLargest = it;
            }
        }
        return secLargest;
    }
};

